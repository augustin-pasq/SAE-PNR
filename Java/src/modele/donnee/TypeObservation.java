package modele.donnee;

/**
 * Enumeration for the types of observation
 *
 * @author Groupe PNR 1D1
 */
public enum TypeObservation {
	/**
	 * The observation is by sound
	 */
	SONORE,
	/**
	 * The observation is visual
	 */
	VISUELLE,
	/**
	 * The observation is visual and by sound
	 */
	SONORE_VISUELLE
}